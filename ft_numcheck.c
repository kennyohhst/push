#include "push_swap.h"

void	ft_numcheck(char *str)
{
	int	i;

	i = 0;
	if ((str[i] == '-' || str[i] == '+') && str[i] != '\0')
		i++;
	if (!(str[i] >= '0' && str[i] <= '9'))
		exit(write(1, "numcheck error 1", 16));
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			exit(write(1, "numcheck error 2", 16));
		i++;
	}
}
