#include "push_swap.h"

void	ft_numcheck(char *str)
{
	int	i;

	i = 0;
	if ((str[i] == '-' || str[i] == '+') && str[i] != '\0')
		i++;
	while (str[i] != '\0')
	{
		if (!(str[i] >= '0' && str[i] <= '9'))
			exit(write(1, "Error", 5));
		i++;
	}
}
